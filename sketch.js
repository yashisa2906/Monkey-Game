var monkey , monkey_running
var banana ,bananaImage, obstacles, obstacleImage
var bananaGroup, obstacleGroup
var score;
var survivalTime=0;

function preload(){
  
  
  monkey_running =            loadAnimation("sprite_0.png","sprite_1.png","sprite_2.png","sprite_3.png","sprite_4.png","sprite_5.png","sprite_6.png","sprite_7.png","sprite_8.png")
  
  bananaImage = loadImage("banana.png");
  obstaceImage = loadImage("obstacle.png");
 
}



function setup() {
  createCanvas(600,200);
  
  monkey = createSprite(100,190,20,20);
  monkey.addAnimation("running", monkey_running);
  monkey.scale=0.1;
  
  ground= createSprite(10,195,1200,20);
  ground.x = ground.width/2;
  ground.velocityX= -4;
  ground.shapeColor = 'oliveDrab';

  
  
  bananaGroup= new Group();
  obstacleGroup= new Group();
  
  score= 0;
  

  
}


function draw() {
background('skyblue');
  monkey.collide(ground); 
  if (ground.x < 0){
      ground.x = ground.width/2;
    }

  if(keyDown('space')&& monkey.y >= 154.2){
    monkey.velocityY =-12;
  }
  monkey.velocityY = monkey.velocityY + 0.8
  
  stroke("white");
  textSize(20);
  fill("white");
  text ('score :'+ score,500,20);
  
  stroke("black");
  textSize(20);
  fill("black");
  survivalTime= Math.ceil(frameCount/frameRate());
  text('Survival Time:'+ survivalTime,50,20)
  
  
drawSprites(); 
spawnObstacles(); 
spawnFood();
 
 
}
  

function spawnObstacles(){
  if(frameCount % 240 === 0){
  obstacles = createSprite(400,170,20,30);
  obstacles.velocityX = -5;
  obstacles.addImage(obstaceImage);
  obstacles.scale=0.09;
   obstacleGroup.add(obstacles);
  }
}

function spawnFood(){
  if(frameCount% 80 === 0 ){
  banana = createSprite(400,120,40,10);
  banana.addImage(bananaImage);
  banana.scale= 0.1;
  banana.y = Math.round(random(30,120));
  banana.velocityX = -5;
  banana.lifetime = -1;  
  bananaGroup.add(banana);
  }
}
  


















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































